"""Export consolidated CSV/JSON/Markdown tables for research artifacts."""

from __future__ import annotations

import argparse
from pathlib import Path
from typing import Any

from research_common import (
    ABLATION_DEFENSE_CONFIGS,
    BY_TYPE_FIELDNAMES,
    COMPARISON_FIELDNAMES,
    CORE_DEFENSE_CONFIGS,
    DEFAULT_OUTPUT_DIR,
    METRIC_FIELDNAMES,
    PROJECT_ROOT,
    build_summary_markdown,
    load_json,
    markdown_table,
    run_research_suite_sync,
    write_artifact_bundle,
    write_csv,
    write_json,
)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Export SentinelAgent research result tables.")
    parser.add_argument("--output-dir", type=Path, default=DEFAULT_OUTPUT_DIR)
    parser.add_argument("--refresh", action="store_true", help="Regenerate benchmark and ablation inputs first.")
    return parser.parse_args()


def ensure_inputs(output_dir: Path, refresh: bool) -> tuple[dict[str, Any], dict[str, Any]]:
    research_path = output_dir / "research_benchmark_results.json"
    ablation_path = output_dir / "ablation_study_results.json"

    if refresh or not research_path.exists():
        research = run_research_suite_sync(
            defense_configs=CORE_DEFENSE_CONFIGS,
            output_dir=output_dir,
            num_runs=1,
            include_benign=True,
            deterministic_timings=True,
        )
        write_artifact_bundle(
            prefix="research_benchmark",
            output_dir=output_dir,
            artifact=research,
            title="SentinelAgent Research Benchmark",
            notes=["Generated by export_results_tables.py --refresh."],
        )

    if refresh or not ablation_path.exists():
        ablation = run_research_suite_sync(
            defense_configs=ABLATION_DEFENSE_CONFIGS,
            output_dir=output_dir,
            num_runs=1,
            include_benign=True,
            deterministic_timings=True,
        )
        ablation["metadata"]["ablation_axis"] = "defense_profile"
        write_artifact_bundle(
            prefix="ablation_study",
            output_dir=output_dir,
            artifact=ablation,
            title="SentinelAgent Ablation Study",
            notes=["Generated by export_results_tables.py --refresh."],
        )

    return load_json(research_path), load_json(ablation_path)


def main() -> int:
    args = parse_args()
    output_dir = Path(args.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    research, ablation = ensure_inputs(output_dir, args.refresh)

    combined_metrics = [
        {**row, "study": "research_benchmark"}
        for row in research.get("metrics", [])
    ] + [
        {**row, "study": "ablation_study"}
        for row in ablation.get("metrics", [])
    ]
    combined_by_type = [
        {**row, "study": "research_benchmark"}
        for row in research.get("by_attack_type", [])
    ] + [
        {**row, "study": "ablation_study"}
        for row in ablation.get("by_attack_type", [])
    ]
    combined_comparisons = [
        {**row, "study": "research_benchmark"}
        for row in research.get("comparisons", [])
    ] + [
        {**row, "study": "ablation_study"}
        for row in ablation.get("comparisons", [])
    ]

    paths = {
        "combined_metrics_csv": output_dir / "combined_metrics.csv",
        "combined_by_type_csv": output_dir / "combined_by_attack_type.csv",
        "combined_comparisons_csv": output_dir / "combined_comparisons.csv",
        "combined_tables_json": output_dir / "combined_tables.json",
        "combined_tables_md": output_dir / "combined_tables.md",
    }

    write_csv(paths["combined_metrics_csv"], combined_metrics, ["study", *METRIC_FIELDNAMES])
    write_csv(paths["combined_by_type_csv"], combined_by_type, ["study", *BY_TYPE_FIELDNAMES])
    write_csv(paths["combined_comparisons_csv"], combined_comparisons, ["study", *COMPARISON_FIELDNAMES])
    write_json(paths["combined_tables_json"], {
        "research_benchmark": research.get("metrics", []),
        "ablation_study": ablation.get("metrics", []),
        "combined_metrics": combined_metrics,
        "combined_by_attack_type": combined_by_type,
        "combined_comparisons": combined_comparisons,
    })

    markdown = [
        "# SentinelAgent Combined Research Tables",
        "",
        "## Research Benchmark",
        "",
        build_summary_markdown("Research Benchmark", research),
        "## Ablation Study",
        "",
        build_summary_markdown("Ablation Study", ablation),
        "## Combined Metrics",
        "",
        markdown_table(
            combined_metrics,
            [
                ("study", "Study"),
                ("defense_config", "Defense"),
                ("attack_success_rate", "ASR"),
                ("benign_task_success_rate", "Benign Success"),
                ("false_positive_rate", "FPR"),
                ("false_negative_rate", "FNR"),
                ("precision", "Precision"),
                ("recall", "Recall"),
                ("f1_score", "F1"),
                ("throughput_qps", "Throughput qps"),
            ],
            percent_columns={
                "attack_success_rate",
                "benign_task_success_rate",
                "false_positive_rate",
                "false_negative_rate",
                "precision",
                "recall",
                "f1_score",
            },
        ),
    ]
    paths["combined_tables_md"].write_text("\n".join(markdown) + "\n", encoding="utf-8")

    print("Wrote exported research tables:")
    for path in paths.values():
        print(f"- {path.resolve().relative_to(PROJECT_ROOT)}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
